package com.hanifbudiarto.flutter_service_plugin.model;

public class AppSettings {

    String themeId;

    public AppSettings(String themeId) {
        this.themeId = themeId;
    }

    public String getThemeId() {
        return themeId;
    }

    public void setThemeId(String themeId) {
        this.themeId = themeId;
    }
}
